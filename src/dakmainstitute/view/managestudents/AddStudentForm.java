    private void fillSchoolCombo() throws SQLException, ClassNotFoundException {
        ArrayList<School> schoolList = SchoolController.getAllSchool();
        schoolCombo.removeAllItems();
        for (School school : schoolList) {
            schoolCombo.addItem(school);

        }
    }

    private void fillSubjectCombo() throws SQLException, ClassNotFoundException {
        ArrayList<Subject> subjectList = SubjectController.getAllSubject();
        for (Subject subject : subjectList) {
            subjectCombo.addItem(subject);

        }
    }
    private void saveButtonSetEnabled() throws SQLException, ClassNotFoundException {
        boolean b=districText.getText().isEmpty()|| classTypeText.getText().isEmpty() || 
                genderText.getText().isEmpty() || indexNotext.getText().isEmpty() || 
                classText.getText().isEmpty() || nameText.getText().isEmpty() || 
                NICText.getText().isEmpty();
        if(b==true){
            SaveButton.setEnabled(false);
        }else{
            SaveButton.setEnabled(true);
        }
    }

