    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        String indexNo = districText.getText() + "" + classTypeText.getText() + "" + genderText.getText() + "" + indexNotext.getText();
        Date d = dobText.getDate();
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String date = df.format(new Date());

        String nic = NICText.getText();
        String name = nameText.getText();
        String dob = df.format(d);
        String address = AddressText.getText();
        String school = ((School) schoolCombo.getSelectedItem()).getSchoolId();
        String mobileNum = mobileNumText.getText();
        String homeNum = homeNumText.getText();
        String gender = null;

        if (maleButton.isSelected()) {
            gender = "Male";
        } else if (femaleButton.isSelected()) {
            gender = "Female";
        }

        String distric = null;

        if (mataraRadioButton.isSelected()) {
            distric = "Matara";
        } else if (galleRadioButton.isSelected()) {
            distric = "Galle";
        } else if (thangallaRadioButton.isSelected()) {
            distric = "Thangalla";
        }

        String classes = classText.getText();
        String subject = subjectCombo.getSelectedItem() + "";

        Student student = new Student(indexNo, nic, name, date, address, dob, school, mobileNum, homeNum, gender, distric, classes, subject);
        int resStudent = 0;
        int resMother = 0;
        int resFather = 0;
        int resGuardian = 0;

        try {
            resStudent = StudentController.addStudent(student);
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AddStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (!mNameText.getText().isEmpty()) {
            Mother mother = new Mother(indexNo, mNameText.getText(), mOccuText.getText(), mAddresstext.getText(), mContactNumText.getText(), mOfiiceNumText.getText());
            try {
                resMother = MotherController.addMother(mother);
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(AddStudentForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        if (!fNameText.getText().isEmpty()) {
            Father father = new Father(indexNo, fNameText.getText(), fOccuText.getText(), fAddresstext.getText(), fContactNumText.getText(), fOfiiceNumText.getText());
            try {
                resFather = FatherController.addFather(father);
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(AddStudentForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        if (!gNameText.getText().isEmpty()) {
            Guardian guardian = new Guardian(indexNo, gNameText.getText(), gOccuText.getText(), gAddresstext.getText(), gContactNumText.getText(), gOfiiceNumText.getText());
            try {
                resGuardian = GuardianController.addGuardian(guardian);
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(AddStudentForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if ((resStudent > 0 || resMother > 0 || resFather > 0 || resGuardian > 0)) {
            JOptionPane.showMessageDialog(this, "Student details added successful...");
        } else {
            JOptionPane.showMessageDialog(this, "Student details added fail...");
        }
        try {
            refresh();
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AddStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SaveButtonActionPerformed

