    private void classTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_classTextKeyReleased
        String txt = classText.getText();
        int caretPosition = classText.getCaretPosition();
        if (!txt.matches("^[0-9]*{4,}+$")) {
            classText.setText(txt.substring(0, caretPosition - 1) + txt.substring(caretPosition));
            classText.setCaretPosition(caretPosition - 1);
        }
        try {
            saveButtonSetEnabled();
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AddStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_classTextKeyReleased

