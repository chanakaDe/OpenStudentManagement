    private String genarateId() throws SQLException, ClassNotFoundException{
        String schoolId=SchoolController.getSchoolId();
        if(schoolId==null){
            return "S0001";
        }else{
            int num=Integer.parseInt(schoolId.substring(2));
            num++;
            if(num < 10){
                return "S000"+num; 
            }else if(num < 100){
                return "S00"+num;
            }else if(num < 1000){
                return "S0"+num;
            }else{
                return "S"+num;
            }
        }
    }
    
    
