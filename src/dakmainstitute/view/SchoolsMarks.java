    private void testIdComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_testIdComboItemStateChanged
        
        DefaultTableModel dtm = (DefaultTableModel) schoolMarksTable.getModel();
        dtm = (DefaultTableModel) schoolMarksTable.getModel();
        dtm.setRowCount(0);
        String testId = testIdCombo.getSelectedItem() + "";
        ArrayList<TestMarks> schoolList = null;
        try {
            schoolList = TestMarksController.getSchoolsRank(testId);
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ViewAvgChartForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        int possision = 0;
        for (TestMarks testMarks : schoolList) {
            Object[] row = {testMarks.getStudent().getSchools().getSchoolName(), testMarks.getAvg(),testMarks.getNumber_of_student()};
            dtm.addRow(row);
            
        }
    }//GEN-LAST:event_testIdComboItemStateChanged

